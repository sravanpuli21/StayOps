#!/usr/bin/env node
/**
 * Connects to Neon, dumps every seedable row to db/seed.sql so the spec's
 * `db/seed.sql` artifact exists alongside the .mjs that generates it.
 *
 * Run after `node db/seed.mjs` so the snapshot is up-to-date:
 *   node db/seed.mjs && node db/dump-seed-sql.mjs
 *
 * The .sql is committable; importing it with `psql` from a clean DB is
 * equivalent to running the .mjs against a clean DB.
 */
import { writeFileSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import postgres from 'postgres';

const __dirname = dirname(fileURLToPath(import.meta.url));
try {
  const envPath = join(__dirname, '..', 'apps', 'web', '.env.local');
  const text = readFileSync(envPath, 'utf8');
  for (const line of text.split('\n')) {
    const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
} catch { /* CI / prod use Vercel-injected env */ }

const url = process.env.DATABASE_URL_UNPOOLED ?? process.env.DATABASE_URL;
if (!url) { console.error('DATABASE_URL_UNPOOLED missing'); process.exit(1); }
const sql = postgres(url, { ssl: 'require', max: 1, prepare: false });

// Tables to dump in dependency order. Reference tables (tenants/users/regions/
// hotels/region_hotels) are emitted via the natural-key upserts they live as
// in seed.mjs — re-deriving them via SQL would lose the on-conflict semantics.
// Instead we wrap them in `select * from tenants` etc and reformat as inserts.
const tables = [
  'tenants','users','regions','hotels','region_hotels',
  'daily_revenue','daily_occupancy',
  'labour_periods','labour_departments',
  'rooms','maintenance_tickets','audit_tasks','employee_records',
  'strategy_annual_targets','strategy_hotel_targets','strategy_initiatives','strategy_capex',
  'assets','asset_hotel_summaries','vendor_spends',
  'am_pm_snapshots','am_pm_room_type_rows',
];

let out = `-- StayOps seed snapshot — generated by db/dump-seed-sql.mjs.
-- Idempotent: run after db/migrate.mjs against a clean DB.
-- Source of truth is db/seed.mjs; this is a derived artifact.

set client_min_messages to warning;
begin;
\n`;

const escVal = (v) => {
  if (v === null) return 'null';
  if (typeof v === 'boolean') return v ? 'true' : 'false';
  if (typeof v === 'number') return String(v);
  if (v instanceof Date) return `'${v.toISOString()}'::timestamptz`;
  if (typeof v === 'object') return `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`;
  return `'${String(v).replace(/'/g, "''")}'`;
};

for (const tbl of tables) {
  const cols = await sql.unsafe(`
    select column_name from information_schema.columns
    where table_schema = 'public' and table_name = $1
    order by ordinal_position
  `, [tbl]);
  if (cols.length === 0) continue;
  const colNames = cols.map((c) => c.column_name)
    // Skip generated columns (`variance_hours` is generated stored).
    .filter((n) => n !== 'variance_hours');
  const rows = await sql.unsafe(`select ${colNames.join(', ')} from "${tbl}"`);
  if (rows.length === 0) continue;
  out += `-- ${tbl}: ${rows.length} rows\n`;
  out += `truncate table "${tbl}" cascade;\n`;
  const valuesLines = rows.map((r) =>
    '(' + colNames.map((c) => escVal(r[c])).join(', ') + ')'
  ).join(',\n  ');
  out += `insert into "${tbl}" (${colNames.map((c) => `"${c}"`).join(', ')}) values\n  ${valuesLines};\n\n`;
}

out += 'commit;\n';
writeFileSync(join(__dirname, 'seed.sql'), out);
console.log(`Wrote db/seed.sql (${out.split('\n').length} lines)`);
await sql.end();
